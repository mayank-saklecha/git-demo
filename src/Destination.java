
public class Destination {

	private String mspId;
	private String cityDetails;
	private String destinationDetails;
	private String locationDetails;
	private String airportDetails;
	public String getMspId() {
		return mspId;
	}
	public void setMspId(String mspId) {
		this.mspId = mspId;
	}
	public String getCityDetails() {
		return cityDetails;
	}
	public void setCityDetails(String cityDetails) {
		this.cityDetails = cityDetails;
	}
	public String getDestinationDetails() {
		return destinationDetails;
	}
	public void setDestinationDetails(String destinationDetails) {
		this.destinationDetails = destinationDetails;
	}
	public String getLocationDetails() {
		return locationDetails;
	}
	public void setLocationDetails(String locationDetails) {
		this.locationDetails = locationDetails;
	}
	public String getAirportDetails() {
		return airportDetails;
	}
	public void setAirportDetails(String airportDetails) {
		this.airportDetails = airportDetails;
	}
}
